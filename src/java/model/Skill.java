package model;

/**
 *
 * @author minhd
 */
public class Skill {
    private int ID;
    private String name;

    public Skill() {
    }

    public Skill(int id, String name) {
        this.ID = id;
        this.name = name;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
